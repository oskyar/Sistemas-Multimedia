    public final void keyboardEvent() {
        KeyboardFocusManager manager = KeyboardFocusManager.getCurrentKeyboardFocusManager();
        manager.addKeyEventDispatcher(new KeyEventDispatcher() {
            @Override
            public boolean dispatchKeyEvent(KeyEvent e) {
                if (e.getID() == KeyEvent.KEY_PRESSED) {
                    if (e.getKeyCode() == e.VK_ESCAPE) {
                        if (vi != null) {
                            vi.getLienzo().getvShapeSelected().clear();
                            repaint();
                            return true;
                        }
                    }
                }
                return false;
            }
        });
    }

    public final VentanaInterna newWindows() {

        final VentanaInterna vi = new VentanaInterna();
        escritorio.add(vi);
        vi.setTitle("Lienzo " + ++numVentanas);
        vi.setVisible(true);
        setVentanaInterna(vi);
        vi.getLienzo().addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                eventoEscritorioShapes(vi);
                repaint();
            }

            @Override
            public void mousePressed(MouseEvent e) {
                eventoEscritorioShapes(vi);
                repaint();
            }

            @Override
            public void mouseReleased(MouseEvent e) {
                eventoEscritorioShapes(vi);
                repaint();
            }
        });
        eventoEscritorioShapes(vi);
        return vi;
    }

    public VentanaInterna selectInternalWindows() {
        VentanaInterna vi = (VentanaInterna) escritorio.getSelectedFrame();
        eventoEscritorioShapes(vi);
        repaint();
        return vi;
    }

